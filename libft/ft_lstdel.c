#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*tmp;

	while (*alst)
	{
		tmp = (*alst)->next;
		del((*alst)->next, (*alst)->content);
		free(*alst);
		*alst = tmp;
	}
	*alst = NULL;
}
